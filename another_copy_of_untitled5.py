# -----------------------------------------------
# HELPER: Score a single nutrient
# -----------------------------------------------
def score_nutrient(value, min_req, max_req):
    if value >= min_req and value <= max_req:
        return "✅ Ideal", 2
    elif value >= min_req * 0.7 and value < min_req:
        return "⚠️  Slightly Low", 1
    elif value > max_req and value <= max_req * 1.3:
        return "⚠️  Slightly High", 1
    elif value < min_req * 0.7:
        return "❌ Deficient", 0
    else:
        return "❌ Excess", 0

# -----------------------------------------------
# HELPER: Fertilizer recommendation
# -----------------------------------------------
def fertilizer_advice(nutrient, current, required_min):
    gap = required_min - current
    if gap <= 0:
        return "No addition needed"
    if nutrient == "N":
        urea_kg = round(gap / 0.46, 1)
        return f"Add ~{gap} kg/ha Nitrogen → approx {urea_kg} kg/ha Urea"
    elif nutrient == "P":
        ssp_kg = round(gap / 0.16, 1)
        return f"Add ~{gap} kg/ha Phosphorus → approx {ssp_kg} kg/ha SSP (Single Superphosphate)"
    elif nutrient == "K":
        mop_kg = round(gap / 0.60, 1)
        return f"Add ~{gap} kg/ha Potassium → approx {mop_kg} kg/ha MOP (Muriate of Potash)"

