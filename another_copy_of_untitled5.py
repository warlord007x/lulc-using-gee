import numpy as np

def create_realistic_mumbai_map_fixed():
    """Generates a synthetic LULC map resembling Greater Mumbai characteristics."""
    # Adjust dimensions for a slightly larger, more detailed map
    height, width = 600, 800
    map_data = np.zeros((height, width), dtype=int)

    # Class definitions:
    # 1: Water, 2: DenseVeg, 3: LightVeg, 4: BuiltUp, 5: Barren, 6: Agriculture, 7: Wetland

    # Simulate coastal areas (West side and some eastern water bodies)
    for i in range(height):
        for j in range(width):
            # Arabian Sea to the west
            if j < width * 0.1:
                map_data[i, j] = 1  # Water
            # Thane Creek / Eastern water bodies
            elif j > width * 0.8 and i > height * 0.2 and i < height * 0.8:
                map_data[i, j] = 1  # Water
            # Patches of water inland (e.g., lakes, reservoirs)
            elif (i > height * 0.1 and i < height * 0.2 and j > width * 0.4 and j < width * 0.45) or \
                 (i > height * 0.7 and i < height * 0.75 and j > width * 0.3 and j < width * 0.35):
                map_data[i, j] = 1  # Water

    # Simulate Sanjay Gandhi National Park (Dense Vegetation in the North)
    sg_np_center_i, sg_np_center_j = height * 0.2, width * 0.5
    for i in range(height):
        for j in range(width):
            dist_to_park = np.sqrt(((i - sg_np_center_i) / (height * 0.2))**2 + ((j - sg_np_center_j) / (width * 0.25))**2)
            if dist_to_park < 1.0 and map_data[i, j] == 0: # Check if not already water
                map_data[i, j] = 2  # Dense Vegetation

    # Simulate Built-up areas (majority of Mumbai)
    # Concentrated in central and southern parts
    for i in range(height):
        for j in range(width):
            if map_data[i, j] == 0: # If not water or dense veg
                if (i > height * 0.3 and j > width * 0.1 and j < width * 0.8) and np.random.random() < 0.85:
                    map_data[i, j] = 4  # Built-up

    # Light Vegetation (parks, urban green spaces, fringes of built-up)
    for i in range(height):
        for j in range(width):
            if map_data[i, j] == 0 and np.random.random() < 0.4: # Remaining areas get light veg
                map_data[i, j] = 3
            elif map_data[i, j] == 4 and np.random.random() < 0.1: # Some light veg within built-up
                 map_data[i, j] = 3

    # Barren lands (small patches, coastal fringes, construction sites)
    for i in range(height):
        for j in range(width):
            if map_data[i, j] == 0 and np.random.random() < 0.15:
                map_data[i, j] = 5

    # Agriculture (more in the eastern/northern fringes, less within the core city)
    for i in range(height):
        for j in range(width):
            if map_data[i, j] == 0 and (j > width * 0.7 or i < height * 0.1) and np.random.random() < 0.3:
                map_data[i, j] = 6

    # Wetlands (near water bodies, estuaries)
    for i in range(height):
        for j in range(width):
            if map_data[i, j] == 0 and \
               ((j > width * 0.08 and j < width * 0.15) or \
                (j > width * 0.78 and j < width * 0.85)) and \
               np.random.random() < 0.2:
                map_data[i, j] = 7
            elif map_data[i, j] == 0 and np.random.random() < 0.05: # Random small patches
                map_data[i, j] = 7

    # Ensure all zeros are filled (default to light vegetation)
    map_data[map_data == 0] = 3

    return map_data

