# -------------------------------
# Define Mumbai circular ROI
# -------------------------------
center = ee.Geometry.Point([72.8777, 19.0760])
roi = center.buffer(35000)  # 35 km radius

# -------------------------------
# Load Sentinel-2 composite
# -------------------------------
s2 = (
    ee.ImageCollection("COPERNICUS/S2_SR")
    .filterBounds(roi)
    .filterDate("2023-01-01", "2023-12-31")
    .filter(ee.Filter.lt("CLOUDY_PIXEL_PERCENTAGE", 10))
    .select(["B2", "B3", "B4", "B8"])
    .median()
    .clip(roi)
)

# -------------------------------
# Add spectral indices (KEY FIX)
# -------------------------------
ndvi = s2.normalizedDifference(["B8", "B4"]).rename("NDVI")
ndwi = s2.normalizedDifference(["B3", "B8"]).rename("NDWI")
s2 = s2.addBands([ndvi, ndwi])

bands = ["B2", "B3", "B4", "B8", "NDVI", "NDWI"]

# -------------------------------
# Training samples (balanced)
# -------------------------------
training_fc = ee.FeatureCollection([
    # Water
    ee.Feature(ee.Geometry.Point([72.79, 18.89]), {"class": 1}),
    ee.Feature(ee.Geometry.Point([72.81, 18.92]), {"class": 1}),
    ee.Feature(ee.Geometry.Point([72.86, 18.90]), {"class": 1}),

    # Dense vegetation
    ee.Feature(ee.Geometry.Point([72.92, 19.25]), {"class": 2}),
    ee.Feature(ee.Geometry.Point([72.95, 19.28]), {"class": 2}),

    # Light vegetation
    ee.Feature(ee.Geometry.Point([72.88, 19.15]), {"class": 3}),
    ee.Feature(ee.Geometry.Point([72.84, 19.18]), {"class": 3}),

    # Built-up
    ee.Feature(ee.Geometry.Point([72.88, 19.08]), {"class": 4}),
    ee.Feature(ee.Geometry.Point([72.90, 19.05]), {"class": 4}),

    # Agriculture
    ee.Feature(ee.Geometry.Point([73.02, 19.22]), {"class": 6}),
])

# -------------------------------
# Train Random Forest classifier
# -------------------------------
training = s2.sampleRegions(
    collection=training_fc,
    properties=["class"],
    scale=10
)

classifier = ee.Classifier.smileRandomForest(200).train(
    training, "class", bands
)

classified = s2.classify(classifier)

